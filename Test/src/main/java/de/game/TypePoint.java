package de.game;
/**
 * 
 * @author Daniel   
 * TypePoint class that is modeling a point with its values for x and y coordinate. 
 *
 */
public class TypePoint {
	
	public final int x;
	public final int y; 
	
	
	/**
	 * 
	 * @param x
	 * @param y
	 */
	public TypePoint(final int x, final int y){
		this.x = x;
		this.y = y;
	}

}
